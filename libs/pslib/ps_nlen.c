#include "pslib.h"

size_t	ps_nlen(char *nbr)
{
	size_t	i;
	size_t	j;

	i = 0;
	j = 0;
	while (nbr[i] && !(nbr[i] >= '1' && nbr[i] <= '9'))
		i++;
	while (nbr[i] >= '0' && nbr[i] <= '9')
	{
		j++;
		i++;
	}
	return (j);
}
